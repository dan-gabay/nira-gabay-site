export const TOPIC_BY_SLUG = new Map(TOPICS.map((t) => [t.slug, t]));
export const TOPIC_BY_TAG = new Map(TOPICS.map((t) => [t.tag, t]));

/** Hub URL for a tag name, or null when the tag has no hub. */
export function topicPathForTag(tag: string): string | null {
  const topic = TOPIC_BY_TAG.get(tag.trim());
  return topic ? `/articles/topic/${topic.slug}` : null;
}
